with CTE as(
    SELECT PLAYER_ID, MIN(EVENT_DATE) AS FRST_DATE
    FROM Activity
    GROUP BY PLAYER_ID),
CON AS(
    SELECT A.PLAYER_ID AS PLAYER_ID
    FROM Activity A
    JOIN
    cte ON A.PLAYER_ID = CTE.PLAYER_ID
    WHERE A.EVENT_DATE=CTE.FRST_DATE + 1
)
