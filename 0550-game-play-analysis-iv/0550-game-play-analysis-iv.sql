SELECT ROUND(COUNT(DISTINCT CON.PLAYER_ID)/COUNT(CTE.PLAYER_ID),2) AS fraction FROM
CTE LEFT JOIN CON ON CON.PLAYER_ID = CTE.PLAYER_ID;
